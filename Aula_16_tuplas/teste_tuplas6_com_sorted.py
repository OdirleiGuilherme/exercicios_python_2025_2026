# Tuplas são imutáveis
# Sorted -> utilizado para organizar por ordem alfabética os item, se alterar
# a tuplas, pois Tuplas são imutáveis.

lanche = ('Hambúrguer', 'Suco', 'Pizza','Pudim','Batata Frita', 'Açai')
print(lanche)

# Usando "Sorted" na segunda impressão em tela, onde houve organização alfabética
# sem alterar os itens da Tupla e sem alterar os índices
print(sorted(lanche))

